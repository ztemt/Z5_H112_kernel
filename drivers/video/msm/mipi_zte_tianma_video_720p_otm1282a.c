void zte_mipi_disp_inc_tianma(unsigned int state)
{
	struct dcs_cmd_req cmdreq;
	unsigned int value;
	value =state;
	

	pr_err("yls...%s value=%d.......\n", __func__, value);
	switch (value) {
	case INTENSITY_NORMAL:
		cmdreq.cmds = sharp_720p_ec_weak_cmd;
		cmdreq.cmds_cnt = ARRAY_SIZE(sharp_720p_ec_weak_cmd);
		
		cmdreq.flags = CMD_REQ_COMMIT;
		cmdreq.rlen = 0;
		cmdreq.cb = NULL;
		
		mipi_dsi_cmdlist_put(&cmdreq);	
		break;
	case INTENSITY_01:

		cmdreq.cmds = sharp_720p_ec_normal_cmd;
		cmdreq.cmds_cnt = ARRAY_SIZE(sharp_720p_ec_normal_cmd);
		
		cmdreq.flags = CMD_REQ_COMMIT;
		cmdreq.rlen = 0;
		cmdreq.cb = NULL;
		
		mipi_dsi_cmdlist_put(&cmdreq);	

		break;
	case INTENSITY_02:
		cmdreq.cmds = sharp_720p_ec_improve_cmd;
		cmdreq.cmds_cnt = ARRAY_SIZE(sharp_720p_ec_improve_cmd);
		
		cmdreq.flags = CMD_REQ_COMMIT;
		cmdreq.rlen = 0;
		cmdreq.cb = NULL;
		
		mipi_dsi_cmdlist_put(&cmdreq);	
		
		break;
	default:

		cmdreq.cmds = sharp_720p_ec_normal_cmd;
		cmdreq.cmds_cnt = ARRAY_SIZE(sharp_720p_ec_normal_cmd);
		
		cmdreq.flags = CMD_REQ_COMMIT;
		cmdreq.rlen = 0;
		cmdreq.cb = NULL;
		
		mipi_dsi_cmdlist_put(&cmdreq);	
		
		break;

	}
}

 void mipi_tianma_set_backlight(struct msm_fb_data_type *mfd)
{	
	struct dcs_cmd_req cmdreq;
	static bool once = true;

	if (mfd->bl_level < 0)
		mfd->bl_level = 0;
	if (mfd->bl_level > MAX_BL_LEVEL_TIANMA)
		mfd->bl_level = MAX_BL_LEVEL_TIANMA;
		
	if (0 == mfd->bl_level) {
		tianma_51[1] = 0x00;
	} else {
		if (mfd->bl_level <= 10) {
			tianma_51[1] = 1;
		} else if (mfd->bl_level < 21){
			tianma_51[1] = 3;
		} else {
			tianma_51[1] = mfd->bl_level - 9;
			if (INTENSITY_01 == v5_intensity_value) {
				tianma_51[1] = tianma_51[1] * 3 / 4;
			}
		}
	}

	pr_err("%s [%s,%d]...level(tianma)=%d, intensity=%d\n", __func__, current->comm,current->pid,mfd->bl_level,v5_intensity_value);
	cmdreq.cmds = tianma_backlight_cmds;
	cmdreq.cmds_cnt = ARRAY_SIZE(tianma_backlight_cmds);
	cmdreq.flags = CMD_REQ_COMMIT;
	cmdreq.rlen = 0;
	cmdreq.cb = NULL;

	mipi_dsi_cmdlist_put(&cmdreq);
	if (mfd->bl_level == 0)
		pr_err("%s [%s,%d]level=%d\n", __func__, current->comm,current->pid,mfd->bl_level);
	if (once){
		//v5_mhl_enable_irq();
		once = false;
	}
}

