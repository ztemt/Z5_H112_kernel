int  mipi_tianma_lcd_on_cmdmode(struct platform_device *pdev)
{
	struct dcs_cmd_req cmdreq;
	struct msm_fb_data_type *mfd;
	static bool is_firsttime = true;

	mfd = platform_get_drvdata(pdev);
	if (!mfd)
		return -ENODEV;
	if (mfd->key != MFD_KEY)
		return -EINVAL;
	if (is_firsttime) {
		zte_mipi_disp_inc_tianma(v5_intensity_value);
		colortmp_turnon();
		is_firsttime = false;
		return 0;
	}
	
	if (!z5mini_lcd_state.disp_initialized) {
		printk("yls==========%s:line%d===========.\n",__func__,__LINE__);
		mipi_set_tx_power_mode(1);
#ifdef TEST_TIANMA
		tianma_read_manufacture_register(mfd, read_id);
		tianma_read_manufacture_register(mfd, tianma_display_on_cmds_part5);
		tianma_read_manufacture_register(mfd, tianma_display_on_cmds_parta);
#endif
		cmdreq.cmds = tianma_display_on_cmds_part_cmdmode;
		cmdreq.cmds_cnt = ARRAY_SIZE(tianma_display_on_cmds_part_cmdmode);
		cmdreq.flags = CMD_REQ_COMMIT;
		cmdreq.rlen = 0;
		cmdreq.cb = NULL;
		mipi_dsi_cmdlist_put(&cmdreq);
		mipi_set_tx_power_mode(0);
		
		/*mipi_set_tx_power_mode(0);
		cmdreq.cmds = r69431_display_on_cmds_part2;
		cmdreq.cmds_cnt = ARRAY_SIZE(r69431_display_on_cmds_part2);
		cmdreq.flags = CMD_REQ_COMMIT;
		cmdreq.rlen = 0;
		cmdreq.cb = NULL;
		mipi_dsi_cmdlist_put(&cmdreq);*/
#ifdef TEST_TIANMA
		tianma_read_manufacture_register(mfd, tianma_display_on_cmds_part5);
		tianma_read_manufacture_register(mfd, tianma_display_on_cmds_parta);
#endif

		zte_mipi_disp_inc_tianma(v5_intensity_value);
		colortmp_turnon();

		z5mini_lcd_state.display_on = TRUE;
		z5mini_lcd_state.disp_initialized = TRUE;

		/*if (is_firsttime) {
			mipi_tianma_backlight_on();
			is_firsttime = false;
		}*/
	}
	pr_err("%s [%s,%d]out\n", __func__, current->comm,current->pid);

	return 0;
}
